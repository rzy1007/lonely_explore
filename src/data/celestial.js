/** 按名称/英文名模糊搜索天体 */
export function searchBodies(query) {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return celestialBodies.filter(
    (b) => b.name.includes(q) || b.nameEn.toLowerCase().includes(q)
  );
}

/** 按层级获取天体 */
export function getBodiesByScale(scale) {
  return celestialBodies.filter((b) => b.scale === scale);
}

/** 获取某天体的子天体 */
export function getChildren(parentId) {
  return celestialBodies.filter((b) => b.parent === parentId);
}
